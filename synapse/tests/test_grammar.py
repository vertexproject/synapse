            parseinfo = {
                'stormcmds': {cmd: {} for cmd in core.stormcmds.keys()},
                'modelinfo': core.model.getModelInfo(),
            }
            # import IPython; IPython.embed()
            for i, query in enumerate(_Queries):
                if i < 0:
                    continue
