        parser = lark.Lark(grammar, start='query', debug=True)

        for i, query in enumerate(_Queries):
            try:

                parser.parse(query)
                # print(f'{tree.pretty()}\n)')
            except (lark.ParseError, lark.UnexpectedCharacters):
                # print(f'Failure on parsing {{{query}}}')
                raise

